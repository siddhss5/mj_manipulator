"""Record a gripper's open→closed joint trajectory from a physics rollout.

Many grippers (Robotiq 2F-85, 2F-140, etc.) have linkage-coupled joints.
To replay their kinematic close accurately, we step physics once with
ctrl commanding full close, then sample joint positions at 101 evenly
spaced time points. The resulting ``(101, n_joints)`` array goes into
``RobotiqGripper(trajectory=...)`` or any similar gripper class.

Output format: a numpy-array literal ready to paste into a Python
module, with a header comment identifying the source model.

Usage::

    uv run python scripts/record_gripper_trajectory.py \\
        --model /path/to/gripper.xml \\
        --actuator fingers_actuator \\
        --ctrl-open 0.0 \\
        --ctrl-closed 255.0 \\
        --joint left_coupler_joint left_driver_joint ... \\
        --steps 800 --samples 101

Defaults match the Robotiq 2F-85 / 2F-140 naming: the joint list
defaults to the standard ``{left,right}_{coupler,driver,follower,spring_link}_joint``
ordering that :class:`mj_manipulator.grippers.robotiq.RobotiqGripper`
expects.
"""

from __future__ import annotations

import argparse
import datetime
import sys
from pathlib import Path

import mujoco
import numpy as np

_DEFAULT_JOINTS = [
    "left_coupler_joint",
    "left_driver_joint",
    "left_follower_joint",
    "left_spring_link_joint",
    "right_coupler_joint",
    "right_driver_joint",
    "right_follower_joint",
    "right_spring_link_joint",
]


def record(
    model_path: Path,
    actuator_name: str,
    joint_names: list[str],
    ctrl_open: float,
    ctrl_closed: float,
    close_steps: int = 800,
    num_samples: int = 101,
) -> np.ndarray:
    """Run close_steps of physics from open → closed, sample ``num_samples`` waypoints.

    Returns an array of shape ``(num_samples, len(joint_names))``.
    """
    model = mujoco.MjModel.from_xml_path(str(model_path))
    data = mujoco.MjData(model)

    # Resolve ids
    aid = mujoco.mj_name2id(model, mujoco.mjtObj.mjOBJ_ACTUATOR, actuator_name)
    if aid < 0:
        raise ValueError(f"No actuator named {actuator_name!r} in {model_path}")
    qpos_indices = []
    for jn in joint_names:
        jid = mujoco.mj_name2id(model, mujoco.mjtObj.mjOBJ_JOINT, jn)
        if jid < 0:
            raise ValueError(f"No joint named {jn!r} in {model_path}")
        qpos_indices.append(model.jnt_qposadr[jid])

    # Start fully open (ctrl=ctrl_open, let settle)
    data.ctrl[aid] = ctrl_open
    mujoco.mj_forward(model, data)
    for _ in range(100):
        mujoco.mj_step(model, data)

    # Record open snapshot
    waypoints: list[np.ndarray] = []
    waypoints.append(np.array([data.qpos[i] for i in qpos_indices], dtype=float))

    # Step toward closed; sample at evenly spaced step counts
    sample_at = np.linspace(0, close_steps, num_samples).astype(int)[1:]
    data.ctrl[aid] = ctrl_closed
    step_count = 0
    for target_step in sample_at:
        while step_count < target_step:
            mujoco.mj_step(model, data)
            step_count += 1
        waypoints.append(np.array([data.qpos[i] for i in qpos_indices], dtype=float))

    return np.stack(waypoints, axis=0)


def format_as_literal(
    trajectory: np.ndarray,
    varname: str,
    source_model: Path,
    joint_names: list[str],
) -> str:
    """Format the trajectory array as a Python module literal."""
    out = []
    today = datetime.date.today().isoformat()
    out.append(f"# Auto-generated by scripts/record_gripper_trajectory.py on {today}.")
    out.append(f"# Source model: {source_model}")
    out.append(f"# Joint order: {joint_names}")
    out.append(f"# Shape: {trajectory.shape} — {trajectory.shape[0]} waypoints, {trajectory.shape[1]} joints.")
    out.append("")
    out.append("import numpy as np")
    out.append("")
    out.append(f"{varname} = np.array(")
    out.append("    [")
    for row in trajectory:
        vals = ", ".join(f"{v:.6f}" for v in row)
        out.append(f"        [{vals}],")
    out.append("    ]")
    out.append(")")
    return "\n".join(out) + "\n"


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument("--model", type=Path, required=True, help="Path to gripper MJCF XML.")
    parser.add_argument(
        "--actuator", type=str, default="fingers_actuator", help="Actuator name (default: fingers_actuator)."
    )
    parser.add_argument("--ctrl-open", type=float, default=0.0, help="Control value for fully-open (default: 0.0).")
    parser.add_argument(
        "--ctrl-closed", type=float, default=255.0, help="Control value for fully-closed (default: 255.0)."
    )
    parser.add_argument(
        "--joint",
        nargs="+",
        default=_DEFAULT_JOINTS,
        help="Joint names in order. Defaults to Robotiq 2F-85/2F-140 naming.",
    )
    parser.add_argument("--steps", type=int, default=800, help="Physics steps for close (default: 800).")
    parser.add_argument("--samples", type=int, default=101, help="Waypoints to sample (default: 101).")
    parser.add_argument(
        "--varname", type=str, default="_GRIPPER_TRAJECTORY", help="Variable name in the output literal."
    )
    parser.add_argument("--out", type=Path, help="Write literal to this file (default: stdout).")
    args = parser.parse_args()

    if not args.model.exists():
        print(f"ERROR: model not found: {args.model}", file=sys.stderr)
        return 1

    traj = record(
        args.model,
        args.actuator,
        args.joint,
        args.ctrl_open,
        args.ctrl_closed,
        close_steps=args.steps,
        num_samples=args.samples,
    )

    print(f"# Recorded: {traj.shape[0]} waypoints × {traj.shape[1]} joints", file=sys.stderr)
    print(f"# First waypoint (open): {traj[0]}", file=sys.stderr)
    print(f"# Last waypoint (closed): {traj[-1]}", file=sys.stderr)

    literal = format_as_literal(traj, args.varname, args.model, args.joint)
    if args.out:
        args.out.write_text(literal)
        print(f"Wrote {args.out}", file=sys.stderr)
    else:
        print(literal)
    return 0


if __name__ == "__main__":
    sys.exit(main())
